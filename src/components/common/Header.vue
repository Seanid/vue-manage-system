<template>
    <div class="header">
        <!-- 折叠按钮 -->
        <div class="collapse-btn" @click="collapseChage">
            <i class="el-icon-menu"></i>
        </div>
        <div class="logo">后台管理系统</div>
        <div class="header-right">
            <div class="header-user-con">
                
                <!-- 系统选择下拉菜单 -->
                <el-dropdown @command="handleSysSelect">
                    <span class="el-dropdown-link">
                        <i class="el-icon-caret-bottom">[系统]{{currSys}}</i>
                    </span>
                    <el-dropdown-menu slot="dropdown">
                        <el-dropdown-item v-for="sys in sysList" :command='sys'>
                                {{sys.name}}
                        </el-dropdown-item>
                    </el-dropdown-menu>
                </el-dropdown>

                <!--间距-->
                <div class="padding-left-20"></div>
                <!-- 消息中心 -->
                <div class="btn-bell">
                    <el-tooltip effect="dark" :content="message?`有${message}条未读消息`:`消息中心`" placement="bottom">
                        <router-link to="/tabs">
                            <i class="el-icon-bell"></i>
                        </router-link>
                    </el-tooltip>
                    <span class="btn-bell-badge" v-if="message"></span>
                </div>
                <!-- 用户头像 -->
                <div class="user-avator"><img src="../../assets/img/img.jpg"></div>
                <!-- 用户名下拉菜单 -->
                <el-dropdown class="user-name" trigger="click" @command="handleCommand">
                    <span class="el-dropdown-link">
                        {{username}} <i class="el-icon-caret-bottom"></i>
                    </span>
                    <el-dropdown-menu slot="dropdown">
                        <el-dropdown-item divided  command="loginout">退出登录</el-dropdown-item>
                    </el-dropdown-menu>
                </el-dropdown>
            </div>
        </div>
    </div>
</template>
<script>
    import bus from '../common/bus';
    import common from '../common/common'
    export default {
        data() {
            return {
                collapse: false,
                name: 'admin',
                message: 0,
                currSys:'用户管理系统',
                sysList:[
                    {name:'用户管理系统',url:'http://baidu.com'},
                    {name:'其他管理系统',url:'http://sina.com'},
                ]
            }
        },
        computed:{
            username(){
                let username = common.getUserName();
                return username ? username : this.name;
            }
        },
        methods:{
            // 用户名下拉菜单选择事件
            handleCommand(command) {
                if(command == 'loginout'){
                    common.logout();
                    this.$router.push('/login');
                }
            },
            // 侧边栏折叠
            collapseChage(){
                this.collapse = !this.collapse;
                bus.$emit('collapse', this.collapse);
            },
            // 系统选择事件
            handleSysSelect(sys){
                if(!sys){
                    return;
                }else{
                    //当前系统不跳转
                    if(sys.name&&sys.name===this.currSys){
                        return ;
                    }else if (sys.name&&sys.name!=this.currSys){
                        //只有https或者http才进行跳转
                        if(sys.url&&(sys.url.startsWith("http")||sys.url.startsWith("https"))){
                            window.open(sys.url,'_blank');
                        }
                    }
                }
                
            }
        },
        mounted(){
            if(document.body.clientWidth < 1500){
                this.collapseChage();
            }
        }
    }
</script>
